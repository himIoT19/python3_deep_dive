# "if" statement for a < 10:
a = 7

if a < 10:
    print(f"a is less than 10")
else:
    print(f"a is greater than 10")

# "if" statement for a = 10:
a = 10
if a < 10:
    print(f"a is less than 10")
elif a == 10:
    print(f"a is equal 10")
else:
    print(f"a is greater than 10")


# Ternary operator:
x = 10 if a < 12 else 20
print(f"Value of x = {x}")
